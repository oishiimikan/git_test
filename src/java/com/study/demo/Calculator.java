package com.study.demo;

public class Calculator {

    public int add(int ops1, int ops2){
        return ops1+ops2;
    }

    public int sub(int ops1, int ops2){
        return ops1-ops2;
    }

    public int multi(int ops1, int ops2){
        return ops1*ops2;
    }

    public double div(double op1, double op2){
        return op1/op2;
    }

}
